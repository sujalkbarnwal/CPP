#include<iostream>
using namespace std;

int main(){

    //Objective: Introduction to different datatypes and their size
    
    char ch;
    int a;
    cout<<"size of char is "<<sizeof(ch)<<endl;
    cout<<"size of int is "<<sizeof(a)<<endl;

    short int si;
    long int li;
    cout<<"size of long int is "<<sizeof(li)<<endl;
    cout<<"size of short int is "<<sizeof(si)<<endl;

    return 0;
}
